function checkSlot() {
  let slot = $('#slot').val();
  switch ( slot ){
    case 'weapon':
      console.log('weapon');
      break;
    case 'helmet':
      console.log('helmet');
      break;
    case 'armor':
      console.log('armor');
      break;
    case 'necklace':
      console.log('necklace');
      break;
    case 'ring':
      console.log('ring');
      break;
    case 'boots':
      console.log('boots');
      break;
    default:
      console.log('error');
  }
}